    private void registerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerButtonActionPerformed
        String name = nameField.getText().trim();
        name = verifyField("Name", name);
        if (name.equals("")) return;
        
        String surname = surnameField.getText().trim();
        surname = verifyField("Surname", surname);
        if (surname.equals("")) return;
        
        String fathersname = fathersnameField.getText().trim();
        fathersname = verifyField("Father's Name", fathersname);
        if (fathersname.equals("")) return;
        
        String mothersname = mothersnameField.getText().trim();
        mothersname = verifyField("Mother's Name", mothersname);
        if (mothersname.equals("")) return;
        
        String strTin = tinField.getText().trim();
        strTin = verifyField("TIN", strTin);
        if (strTin.equals("")) return;
        int tin = verifyInt("TIN", strTin);
        if (tin == -1) return;
        
        String strSsn = ssnField.getText().trim();
        strSsn = verifyField("SSN", strSsn);
        if (strSsn.equals("")) return;
        int ssn = verifyInt("SSN", strSsn);
        if (ssn == -1) return;
        
        String address = addressField.getText().trim();
        address = verifyField("Address", address);
        if (address.equals("")) return;
        
        String email1 = email1Field.getText().trim();
        email1 = verifyField("Email 1", email1);
        if (email1.equals("")) return;
        
        String email2 = email2Field.getText().trim();
        email2 = verifyField("Email 2", email2);
        if (email2.equals("")) return;
        
        String employeetype = employeetypeField.getText().trim();
        employeetype = verifyField("Employee Type", employeetype);
        if (employeetype.equals("")) return;
        
        String strVacancies = vacanciesField.getText().trim();
        strVacancies = verifyField("Vacancies", strVacancies);
        if (strVacancies.equals("")) return;
        int vacancies = verifyInt("Vacancies", strVacancies);
        if (vacancies == -1) return;
        
        String strWorkhours = workhoursField.getText().trim();
        strWorkhours = verifyField("Work Hours", strWorkhours);
        if (strWorkhours.equals("")) return;
        float workhours = verifyFloat("Work Hours", strWorkhours);
        if (workhours == -1) return;
        
        String salarytype = salarytypeField.getText().trim();
        salarytype = verifyField("Salary Type", salarytype);
        if (salarytype.equals("")) return;
        
        String strKidcount = kidcountField.getText().trim();
        strKidcount = verifyField("Kid Count", strKidcount);
        if (strKidcount.equals("")) return;
        int kidcount = verifyInt("Kid Count", strKidcount);
        if (kidcount == -1) return;
        
        String maritalstatus = maritalstatusField.getText().trim();
        maritalstatus = verifyField("Marital Status", maritalstatus);
        if (maritalstatus.equals("")) return;
        
        String educationlevel = educationlevelField.getText().trim();
        educationlevel = verifyField("Education Level", educationlevel);
        if (educationlevel.equals("")) return;
                
        Connection conn = null;
        
        try {            
            conn = DriverManager.getConnection(DBCredentials.CONN_STRING, DBCredentials.USERNAME, DBCredentials.PASSWORD);
            Statement stmt = conn.createStatement();
            
            String st = "INSERT INTO `employees` (`id`, `name`, `surname`, `father_name`, `mother_name`, `TIN`, `SSN`, `address`, `email_1`, `email_2`, `employee_type`, `vacancies`, `work_hours`, `salary_type`, `kid_count`, `marital_status`, `education_level`) VALUES (NULL, '";
            st = st + name + "', '";
            st = st + surname + "', '";
            st = st + fathersname + "', '";
            st = st + mothersname + "', '";
            st = st + tin + "', '";
            st = st + ssn + "', '";
            st = st + address + "', '";
            st = st + email1 + "', '";
            st = st + email2 + "', '";
            st = st + employeetype + "', '";
            st = st + vacancies + "', '";
            st = st + workhours + "', '";
            st = st + salarytype + "', '";
            st = st + kidcount + "', '";
            st = st + maritalstatus + "', '";
            st = st + educationlevel + "')";
            
            stmt.executeUpdate(st);
                        
            conn.close();
            
            JOptionPane.showMessageDialog(null, "Employee registered!", "Employee Registered", JOptionPane.INFORMATION_MESSAGE);
        
            DatabasePanel databasePanel = new DatabasePanel();
            databasePanel.setLocationRelativeTo(null);
            databasePanel.setVisible(true);
            databasePanel.isAdmin = isAdmin;
        
            this.dispose();
        }
        catch (SQLException e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_registerButtonActionPerformed

