    private float verifyFloat(String fieldName, String strNumber) {
        float number;
        
        try {
            number = Float.valueOf(strNumber);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, fieldName + " is not valid!", "Invalid " + fieldName, JOptionPane.WARNING_MESSAGE);
        
            number = -1;
        }
        
        return number;
    }
    
    private int verifyInt(String fieldName, String strNumber) {
        int number;
        
        try {
            number = Integer.parseInt(strNumber);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, fieldName + " is not valid!", "Invalid " + fieldName, JOptionPane.WARNING_MESSAGE);
        
            number = -1;
        }
        
        return number;
    }
    
    private String verifyField(String fieldName, String field) {
        if (field.length() < 1) {
            JOptionPane.showMessageDialog(null, fieldName + " is not valid!", "Invalid " + fieldName, JOptionPane.WARNING_MESSAGE);
        
            return "";
        }
        
        return field;
    }

